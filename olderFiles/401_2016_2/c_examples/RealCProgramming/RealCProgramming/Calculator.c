#include "Calculator.h"

int Calculator_add(int a, int b)
{
    return a + b;
}

int Calculator_sub(int a, int b)
{
    return a - b;
}

int Calculator_div(int a, int b)
{
    return a / b;
}

int Calculator_mul(int a, int b)
{
    return a * b;
}