#import <Foundation/Foundation.h>

int main(int argc, const char * argv[]) {
    @autoreleasepool
    {
        // Object-C
        NSNumber *number = @1;
        // C number
        int x = 2;
        
        int sum = x + number.intValue;
        NSLog(@"The sum of %@ and %i is %i", number, x, sum);
        
        String.alloc();
        NSString *string = [NSString alloc];
    }
    return 0;
}
